#include "DLP_Color.h"
#include "utildefines.h"

//////////////////////////////////////////////////////
//
//	Constructor
//
//////////////////////////////////////////////////////
CDLP_Color::CDLP_Color(float tr,float tg,float tb)
{
	r	= tr;
	g	= tg;
	b	= tb;
}

//////////////////////////////////////////////////////
//
//	Destructor
//
//////////////////////////////////////////////////////
CDLP_Color::~CDLP_Color()
{

}


//////////////////////////////////////////////////////
//
//	Set
//
//////////////////////////////////////////////////////
void CDLP_Color::vSet(float tr,float tg,float tb)
{
	r	= tr;
	g	= tg;
	b	= tb;
}


//////////////////////////////////////////////////////
//
//	Set
//
//////////////////////////////////////////////////////
void CDLP_Color::vSet(CDLP_Color *poColor)
{
	r	= poColor->r;
	g	= poColor->g;
	b	= poColor->b;
}


//////////////////////////////////////////////////////
//
//	Get Red
//
//////////////////////////////////////////////////////
float CDLP_Color::fGetRed(void)
{
	return r;
}


//////////////////////////////////////////////////////
//
//	Get Green
//
//////////////////////////////////////////////////////
float CDLP_Color::fGetGreen(void)
{
	return g;
}


//////////////////////////////////////////////////////
//
//	Get Blue
//
//////////////////////////////////////////////////////
float CDLP_Color::fGetBlue(void)
{
	return b;
}